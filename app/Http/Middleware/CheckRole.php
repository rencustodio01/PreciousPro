<?php

namespace App\Http\Middleware;

use Closure;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Log;
use Symfony\Component\HttpFoundation\Response;

class CheckRole
{
    public function handle(Request $request, Closure $next, string ...$roles): Response
    {
        if (!auth()->check()) {
            return redirect()->route('login');
        }

        if (!session()->has('auth_token')) {
            session(['auth_token' => bin2hex(random_bytes(32))]);
        }

        // If user has no role assigned yet, redirect to pending page
        if (!auth()->user()->role_id) {
            return redirect()->route('pending');
        }

        $userRole = auth()->user()->role?->role_name;

        if (!in_array($userRole, $roles)) {
            Log::channel('daily')->warning('Unauthorized Access Attempt', [
                'user_id'        => auth()->id(),
                'user'           => auth()->user()->full_name,
                'role'           => $userRole,
                'required_roles' => $roles,
                'url'            => $request->fullUrl(),
                'ip'             => $request->ip(),
                'timestamp'      => now()->toDateTimeString(),
            ]);

            abort(403, 'Unauthorized action. Insufficient role permissions.');
        }

        return $next($request);
    }
}




















































































































































































































































































































































































































































































































































































